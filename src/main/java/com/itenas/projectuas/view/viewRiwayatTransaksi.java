    public final void getData() {
        DefaultTableModel dtm = (DefaultTableModel) tbl_histori.getModel();
        dtm.setRowCount(0);

        List<Transaksi> listTransaksi = conTrans.showTransaksi(user);

        for (Transaksi newTransaksi : listTransaksi) {
            Object[] data = {
                newTransaksi.getInvoice(),
                newTransaksi.getNamaHewan(),
                Double.toString(newTransaksi.getBerat()),
                Double.toString(newTransaksi.getHarga()),
                DateTimeFormatter.ofPattern("dd-MM-yyyy").format(newTransaksi.getTanggalBeli()),
                newTransaksi.getNama(),
                newTransaksi.getAlamat(),
                newTransaksi.getHewanId(),
                DateTimeFormatter.ofPattern("dd-MM-yyyy").format(newTransaksi.getTanggalBeli()), // Assuming Tanggal_Beli is the same
                newTransaksi.getNamaTujuan(),
                newTransaksi.getJenisBank(),
                Double.toString(newTransaksi.getSaldo()),
                Double.toString(newTransaksi.getTagihan()),
                Double.toString(newTransaksi.getSelisih())
            };

            dtm.addRow(data);
        }
    }

